//Entrada e Dados e Armazenamento
let numero1 = parseFloat(prompt("Digite o 1 número: "))
let numero2 = parseFloat(prompt("Digite o 2 número: "))

//Processamento
let soma = (numero1 + numero2)
let subtracao = (numero1 - numero2)
let multiplicacao = (numero1 * numero2)
let divisao = (numero1 / numero2)


//Saída de Dados

document.write("<br>Soma: " + soma)
document.write("<br>subtracao: " + subtracao)
document.write("<br>multiplicacao: " + multiplicacao)
document.write("<br>divisao: " + divisao)